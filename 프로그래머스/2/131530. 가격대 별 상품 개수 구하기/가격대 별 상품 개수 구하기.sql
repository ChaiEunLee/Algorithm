WITH P AS (
        SELECT *, TRUNCATE(PRICE/10000,0) AS 'PG'
        FROM PRODUCT)
        
SELECT PG*10000 AS PRICE_GROUP, COUNT(PRODUCT_ID)
FROM P
GROUP BY PG
ORDER BY 1 ASC
